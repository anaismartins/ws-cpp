#include <iostream>

using namespace std;

int main()
{

    int a = 10;
    int b = 5;

    cout << "equal to: " << (a == b) << endl; // has to have the parenthesis
    cout << "not equal to: " << (a != b) << endl;
    cout << "greater than: " << (a > b) << endl;
    cout << "less than: " << (a < b) << endl;
    cout << "greater than or equal to: " << (a >= b) << endl;
    cout << "less than or equal to: " << (a <= b) << endl;

    return 0;
}